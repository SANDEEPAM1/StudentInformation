   public void Load(){
       
       ImpMethod im = new ImpMethod();
       List <Model> list = im.list();
       DefaultTableModel DFT =  (DefaultTableModel) jTable1.getModel();
       DFT.setRowCount(0);
       for(Model obj : list)
       {
           int sid = obj.getId();
           String sname = obj.getName();
           String scourse = obj.getCourse();
           int sfee = obj.getFee();
           DFT.addRow(new Object[]{sid,sname,scourse,sfee});
       }
       
       
       
   }
    
    
    
    
    
