    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Model obj = new Model();
        String tName = textName.getText();
        String tCourse = textCourse.getText();
        int tFee = Integer.parseInt(textFee.getText());
        
        obj.setName(tName);
        obj.setCourse(tCourse);
        obj.setFee(tFee);
        
        ImpMethod im = new ImpMethod();
        im.save(obj);
        
        Load();
        
        textName.setText("");
        textCourse.setText("");
        textFee.setText("");
        textName.requestFocus();
        
        
        
        
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    
